    if has_nodule:
        results.append({
            "type": "肺结节",
            "formula": LUNG_NODULE_FORMULA["name"],
            "details": LUNG_NODULE_FORMULA,
            "match_score": 10
        })
    for formula_name, formula_info in FORMULAS.items():
        score = 0
        matched_symptoms = []
        for symptom in symptoms:
            if symptom in formula_info.get("症状", []):
                score += 1
                matched_symptoms.append(symptom)
        if score > 0:
            results.append({
                "type": "经典方剂",
                "formula": formula_name,
                "details": formula_info,
                "matched_symptoms": matched_symptoms,
                "match_score": score
            })
    results.sort(key=lambda x: x["match_score"], reverse=True)
    return results[:5]

